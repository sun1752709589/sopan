package entity;

public class ITBean {

	private String id;
	private String siteName;
	private String siteMain;
	private String siteBlog;
	private String siteBbs;
	private String siteDownload;
	private String siteAsk;
	private String siteNews;



	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public String getSiteMain() {
		return siteMain;
	}

	public void setSiteMain(String siteMain) {
		this.siteMain = siteMain;
	}

	public String getSiteBlog() {
		return siteBlog;
	}

	public void setSiteBlog(String siteBlog) {
		this.siteBlog = siteBlog;
	}

	public String getSiteBbs() {
		return siteBbs;
	}

	public void setSiteBbs(String siteBbs) {
		this.siteBbs = siteBbs;
	}

	public String getSiteDownload() {
		return siteDownload;
	}

	public void setSiteDownload(String siteDownload) {
		this.siteDownload = siteDownload;
	}

	public String getSiteAsk() {
		return siteAsk;
	}

	public void setSiteAsk(String siteAsk) {
		this.siteAsk = siteAsk;
	}

	public String getSiteNews() {
		return siteNews;
	}

	public void setSiteNews(String siteNews) {
		this.siteNews = siteNews;
	}
}
